import math

a = 2
b = 3

print(0 - a) # makes numbers negative

print(abs(- a)) # makes numbers positive

print(a * a) # area of square

print(a * b) # area of rectangle

print((4 / 3) * math.e * (a ** 3)) # volume of a sphere

print((a - 32) * (5 / 9)) # fahrenheit to celsius

print(a * 1.60934) #mph to kph
